    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        Cliente c = new Cliente();
        try{
            c.setCpf(txtCpf.getText());
            c.setNome(txtNome.getText());
            c.setCelular(txtCelular.getText());
            c.setDataNasc(LocalDate.parse(txtFormatDataNasc.getText(), DateTimeFormatter.ofPattern("dd/MM/y")));
            c.setCep(txtCep.getText());
            c.setRua(txtRua.getText());
            c.setComplemento(txtComplemento.getText());
            c.setPontoReferencia(txtPontoRef.getText());
            
            ClienteDAO cdao = new ClienteDAO();
            cdao.cadastrarC(c);
            JOptionPane.showMessageDialog(this, "Cliente cadastrado com sucesso.");
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar: "+e.getMessage());
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

