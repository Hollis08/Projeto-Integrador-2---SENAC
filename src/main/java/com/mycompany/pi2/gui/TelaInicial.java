    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        int idSelecionado = tblProduto.getSelectedRow();
        
        if(idSelecionado == -1){
            JOptionPane.showMessageDialog(this, "Selecione um produto." );
        }else{
            try{
            ProdutoDAO pdao = new ProdutoDAO();
            Object valorColuna = tblProduto.getValueAt(idSelecionado, 0);
            int id = Integer.parseInt(valorColuna.toString());
            pdao.excluir(id);
            }catch(Exception e){
                JOptionPane.showMessageDialog(this, "Erro ao excluir" + e.getMessage());
            }
        }
        
        
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
          //criando nova tela de cadastro
          CadastroProduto cadastro = new CadastroProduto();        
          
          if(tblProduto.getSelectedRow() >= 0){ //há linha selecionada na tabela?
              String id = (String)tblProduto.getValueAt(tblProduto.getSelectedRow(), 0);
              
              ProdutoDAO pDao = new ProdutoDAO();
              Produto despesaSelecionada = pDao.obter(Integer.parseInt(id));
              
              cadastro.preencheEdicao(despesaSelecionada);
              cadastro.setVisible(true);
          }
      }catch(Exception e){
          JOptionPane.showMessageDialog(this, "Ocorreu uma falha:\n" + e.getMessage());
      }
    }//GEN-LAST:event_jButton1ActionPerformed

