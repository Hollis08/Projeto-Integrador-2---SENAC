        public void preencherTabela(List<Produto> produto){
        String columns[] = {"Id","CodBarra","Nome","Valor","Quantidade","Tipo","Fabricante","Fornecedor"};
        String dados[][] = new String[produto.size()][columns.length];
        
            int i=0;
            for(Produto p : produto){
                dados[i] = new String[]{
                    String.valueOf(p.getId()),
                    p.getCodBarra(),
                    p.getNome(),
                    String.valueOf(p.getValor()),
                    String.valueOf(p.getQuantidade()),
                    p.getTipo(),
                    p.getFabricante(),
                    p.getFornecedor(),
                };
                i++;
            }
            
            DefaultTableModel model = new DefaultTableModel(dados, columns);
            tblProduto.setModel(model);
        }
        



